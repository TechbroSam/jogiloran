// sanity/schemaTypes/category.ts

import {defineField, defineType} from 'sanity'

export default defineType({
  name: 'category',
  title: 'Category',
  type: 'document',
  fields: [
    defineField({
      name: 'name',
      title: 'Category Name',
      type: 'string',
      validation: (Rule) => Rule.required(),
    }),
    defineField({
      name: 'slug',
      title: 'Slug',
      type: 'slug',
      options: {
        source: 'name',
        maxLength: 96,
      },
      validation: (Rule) => Rule.required(),
    }),
    // Add this new image field
    defineField({
      name: 'image',
      title: 'Category Image',
      type: 'image',
      validation: (Rule) => Rule.required(),
    }),
  ],
})